# String Syntax advanced
# \'    single quote
# \"    double quote
# \t    tab
# \n    newline (line break)
# \\    backslash

# to start a new line in string use "\n"
hello = 'Hello there my friends! \nHow are you? \nStay home and stay safe huh?'
print(hello)
print("Double 'quotes'. That\'s great.")

# raw string - everything will be displayed
print(r'That is a \'raw\' string.')

# multi line string """ """
print("""Dear Mr. Smith,

Hope you  are well.
Please check your email for more information.

Sincerely,
Neo""")
